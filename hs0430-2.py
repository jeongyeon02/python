print("x, y, z 3개의 값을 입력하시오")   # 문자형 x, y, z 3개의 값을 입력하시오 출력

x = int(input("x 값 입력: "))            # 숫자형으로 선언된 변수 x 값 입력
y = int(input("y 값 입력: "))            # 숫자형으로 선언된 변수 y 값 입력
z = int(input("z 값 입력: "))            # 숫자형으로 선언된 변수 z 값 입력

x = x + 1                                # 변수 x를 x + 1로 선언
y = y + 1                                # 변수 y를 y + 1로 선언
z = z + 1                                # 변수 z를 z + 1로 선언

print("변경된 값은?", x, y, z)           # 문자형 변경된 값은?과 변수 x, y, z 출력
